    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
       // Pakai ukuran kertas A5 agar menyerupai struk besar
        com.lowagie.text.Document document = new com.lowagie.text.Document(com.lowagie.text.PageSize.A5);
        try {
            // Pastikan uang bayar sudah diisi sebelum nyetak struk
            if (txtBayar.getText().isEmpty() || txtKembalian.getText().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Isi uang bayar terlebih dahulu sebelum cetak struk!");
                return;
            }

            String namaFile = "Struk_Belanja.pdf";
            com.lowagie.text.pdf.PdfWriter.getInstance(document, new java.io.FileOutputStream(namaFile));
            document.open();
            
            com.lowagie.text.Font fontJudul = com.lowagie.text.FontFactory.getFont(com.lowagie.text.FontFactory.HELVETICA_BOLD, 14);
            com.lowagie.text.Paragraph judul = new com.lowagie.text.Paragraph("STRUK PEMBELANJAAN\nKASIR DIGITAL ANNISA\n\n", fontJudul);
            judul.setAlignment(com.lowagie.text.Element.ALIGN_CENTER);
            document.add(judul);
            
            // Tabel berisi daftar barang (4 kolom)
            com.lowagie.text.pdf.PdfPTable table = new com.lowagie.text.pdf.PdfPTable(4);
            table.setWidthPercentage(100);
            table.addCell("Nama Barang");
            table.addCell("Harga");
            table.addCell("Qty");
            table.addCell("Subtotal");
            
            // Loop untuk mengambil data dari keranjang yang sedang tampil
            for (int i = 0; i < keranjangDatang.getRowCount(); i++) {
                table.addCell(keranjangDatang.getValueAt(i, 1).toString()); // Kolom Nama
                table.addCell(keranjangDatang.getValueAt(i, 2).toString()); // Kolom Harga
                table.addCell(keranjangDatang.getValueAt(i, 3).toString()); // Kolom Qty
                table.addCell(keranjangDatang.getValueAt(i, 4).toString()); // Kolom Subtotal
            }
            document.add(table);
            
            // Ringkasan harga di bagian bawah (rata kanan)
            com.lowagie.text.Paragraph ringkasan = new com.lowagie.text.Paragraph(
                "\n------------------------------------------------------" +
                "\nTotal Belanja : Rp " + txtTotalBelanja.getText() +
                "\nDiskon        : " + txtDiskon.getText() + "%" +
                "\nPPN (10%)     : Rp " + txtPpn.getText() +
                "\nGRAND TOTAL   : Rp " + txtGrandTotal.getText() +
                "\nUang Bayar    : Rp " + txtBayar.getText() +
                "\nKembalian     : Rp " + txtKembalian.getText() +
                "\n------------------------------------------------------" +
                "\n\nTerima Kasih Atas Kunjungan Anda!"
            );
            ringkasan.setAlignment(com.lowagie.text.Element.ALIGN_RIGHT);
            document.add(ringkasan);
            
            document.close();
            javax.swing.JOptionPane.showMessageDialog(this, "Cetak Struk Berhasil!");
            
            // Langsung buka struk PDF-nya
            java.awt.Desktop.getDesktop().open(new java.io.File(namaFile));
            
        } catch (Exception e) {
            if (document.isOpen()) document.close();
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Mencetak Struk: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCetakActionPerformed

