    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        try {
            java.sql.Connection conn = KoneksiDB.getKoneksi();
            
            // 1. Membuat Nomor Transaksi Otomatis berdasarkan Waktu (Unique)
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyyMMddHHmmss");
            String noTransaksi = "TRX-" + sdf.format(new java.util.Date());
            
            // 2. Simpan ke tabel tb_transaksi
            String sqlTransaksi = "INSERT INTO tb_transaksi VALUES (?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pstTx = conn.prepareStatement(sqlTransaksi);
            pstTx.setString(1, noTransaksi);
            // Mengisi tanggal hari ini
            pstTx.setString(2, new java.text.SimpleDateFormat("yyyy-MM-dd").format(new java.util.Date()));
            pstTx.setInt(3, Integer.parseInt(txtGrandTotal.getText()));
            pstTx.setInt(4, Integer.parseInt(txtBayar.getText()));
            pstTx.setInt(5, Integer.parseInt(txtKembalian.getText()));
            pstTx.execute();
            
            // 3. Looping isi keranjang untuk simpan ke tb_detail & POTONG STOK
            String sqlDetail = "INSERT INTO tb_detail VALUES (?, ?, ?, ?, ?)";
            String sqlUpdateStok = "UPDATE tb_barang SET stok = stok - ? WHERE kode_barang = ?";
            
            java.sql.PreparedStatement pstDt = conn.prepareStatement(sqlDetail);
            java.sql.PreparedStatement pstStok = conn.prepareStatement(sqlUpdateStok);
            
            for (int i = 0; i < keranjangDatang.getRowCount(); i++) {
                String kode = keranjangDatang.getValueAt(i, 0).toString();
                int harga = Integer.parseInt(keranjangDatang.getValueAt(i, 2).toString());
                int qty = Integer.parseInt(keranjangDatang.getValueAt(i, 3).toString());
                int subtotal = Integer.parseInt(keranjangDatang.getValueAt(i, 4).toString());
                
                // Set parameter untuk tb_detail
                pstDt.setString(1, noTransaksi);
                pstDt.setString(2, kode);
                pstDt.setInt(3, harga);
                pstDt.setInt(4, qty);
                pstDt.setInt(5, subtotal);
                pstDt.addBatch(); // Kumpulkan dulu
                
                // Set parameter untuk potong stok di tb_barang
                pstStok.setInt(1, qty);
                pstStok.setString(2, kode);
                pstStok.addBatch(); // Kumpulkan dulu
            }
            
            // Eksekusi semua data detail dan update stok secara massal (Batch)
            pstDt.executeBatch();
            pstStok.executeBatch();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Transaksi Berhasil Disimpan!\nNomor: " + noTransaksi);
            
            // Mengosongkan keranjang asal karena transaksi sudah selesai
            keranjangDatang.setRowCount(0);
            this.dispose();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memproses transaksi: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSelesaiActionPerformed

