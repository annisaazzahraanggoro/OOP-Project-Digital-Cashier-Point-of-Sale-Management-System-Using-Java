    private void hitungPembayaran() {
    try {
        int total = Integer.parseInt(txtTotalBelanja.getText());

        // Ambil angka diskon (jika kosong, anggap 0)
        int diskonPersen = 0;
        if (!txtDiskon.getText().isEmpty()) {
            diskonPersen = Integer.parseInt(txtDiskon.getText());
        }

        int nominalDiskon = (total * diskonPersen) / 100;
        int ppn = (total * 10) / 100;
        txtPpn.setText(String.valueOf(ppn));

        int grandTotal = total - nominalDiskon + ppn;
        txtGrandTotal.setText(String.valueOf(grandTotal));

        // Hitung kembalian jika uang bayar sudah diisi angka
        if (!txtBayar.getText().isEmpty()) {
            int bayar = Integer.parseInt(txtBayar.getText());
            int kembalian = bayar - grandTotal;
            txtKembalian.setText(String.valueOf(kembalian));
        } else {
            txtKembalian.setText("");
        }
    } catch (Exception e) {
        // Abaikan jika ada error ketik
    }
}
