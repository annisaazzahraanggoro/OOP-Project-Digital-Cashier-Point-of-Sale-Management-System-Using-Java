    private void load_laporan() {
     DefaultTableModel model = new DefaultTableModel();
     model.addColumn("No Transaksi");
     model.addColumn("Tanggal");
     model.addColumn("Total Belanja");
     model.addColumn("Bayar");
     model.addColumn("Kembalian");

     try {
         // Mengambil data dari tabel transaksi di database
         String sql = "SELECT * FROM tb_transaksi ORDER BY tanggal DESC"; 
         Connection conn = KoneksiDB.getKoneksi();
         Statement stm = conn.createStatement();
         ResultSet res = stm.executeQuery(sql);

         while (res.next()) {
             model.addRow(new Object[]{
                 res.getString("no_transaksi"),
                 res.getString("tanggal"),
                 res.getString("total_belanja"),
                 res.getString("bayar"),
                 res.getString("kembalian")
             });
         }
         tblLaporan.setModel(model);
     } catch (Exception e) {
         JOptionPane.showMessageDialog(this, "Gagal memuat laporan: " + e.getMessage());
     }
 }
    
