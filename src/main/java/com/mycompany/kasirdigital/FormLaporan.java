    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        com.lowagie.text.Document document = new com.lowagie.text.Document(com.lowagie.text.PageSize.A4);
        try {
            String namaFile = "Laporan_Penjualan.pdf";
            com.lowagie.text.pdf.PdfWriter.getInstance(document, new java.io.FileOutputStream(namaFile));
            document.open();
            
            com.lowagie.text.Font fontJudul = com.lowagie.text.FontFactory.getFont(com.lowagie.text.FontFactory.HELVETICA_BOLD, 16);
            com.lowagie.text.Paragraph judul = new com.lowagie.text.Paragraph("LAPORAN PENJUALAN HARIAN\n\n", fontJudul);
            judul.setAlignment(com.lowagie.text.Element.ALIGN_CENTER);
            document.add(judul);
            
            // Membuat tabel dengan 5 kolom
            com.lowagie.text.pdf.PdfPTable table = new com.lowagie.text.pdf.PdfPTable(5);
            table.setWidthPercentage(100);
            
            // Judul kolom
            String[] headers = {"No Transaksi", "Tanggal", "Total Belanja", "Bayar", "Kembalian"};
            for (String h : headers) {
                com.lowagie.text.pdf.PdfPCell cell = new com.lowagie.text.pdf.PdfPCell(new com.lowagie.text.Phrase(h));
                cell.setHorizontalAlignment(com.lowagie.text.Element.ALIGN_CENTER);
                cell.setBackgroundColor(java.awt.Color.LIGHT_GRAY);
                table.addCell(cell);
            }
            
            // Ambil data dari database
            java.sql.Connection conn = KoneksiDB.getKoneksi();
            java.sql.Statement stat = conn.createStatement();
            java.sql.ResultSet rs = stat.executeQuery("SELECT * FROM tb_transaksi ORDER BY tanggal DESC");
            
            while (rs.next()) {
                table.addCell(rs.getString("no_transaksi"));
                table.addCell(rs.getString("tanggal"));
                table.addCell(rs.getString("total_belanja"));
                table.addCell(rs.getString("bayar"));
                table.addCell(rs.getString("kembalian"));
            }
            document.add(table);
            document.close();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Cetak Laporan Berhasil!\nFile: " + namaFile);
            
            // Langsung otomatis buka file PDF-nya
            java.awt.Desktop.getDesktop().open(new java.io.File(namaFile));
            
        } catch (Exception e) {
            if (document.isOpen()) document.close();
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Mencetak: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCetakActionPerformed

