    private void txtKodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKodeKeyReleased
        try {
            java.sql.Connection conn = KoneksiDB.getKoneksi();
            java.sql.Statement stm = conn.createStatement();
            // Mencari barang di database berdasarkan kode yang diketik
            java.sql.ResultSet res = stm.executeQuery("SELECT * FROM tb_barang WHERE kode_barang='" + txtKode.getText() + "'");
            
            if (res.next()) {
                // Jika barang ditemukan, otomatis isi nama dan harga
                txtNama.setText(res.getString("nama_barang"));
                txtHarga.setText(res.getString("harga"));
            } else {
                // Jika tidak ditemukan, kosongkan lagi
                txtNama.setText("");
                txtHarga.setText("");
            }
        } catch (Exception e) {
            // Tampilkan error di output bawah NetBeans agar kita tahu kalau ada yang salah dengan databasenya
            System.out.println("Error Auto-fill: " + e.getMessage());
        }
    }//GEN-LAST:event_txtKodeKeyReleased

