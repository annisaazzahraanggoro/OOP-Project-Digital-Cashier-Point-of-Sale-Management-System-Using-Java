private void hitungTotal() {
        int total = 0;
        for (int i = 0; i < tblKeranjang.getRowCount(); i++) {
            // Mencegah error kalau tidak sengaja ada baris kosong
            Object nilaiSubtotal = tblKeranjang.getValueAt(i, 4);
            if (nilaiSubtotal != null) {
                total += Integer.parseInt(nilaiSubtotal.toString());
            }
        }
        // Tampilkan ke textfield total sementara
        txtTotalSementara.setText(String.valueOf(total));
    }
