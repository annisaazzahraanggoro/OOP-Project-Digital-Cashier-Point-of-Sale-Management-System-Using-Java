    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            // Cek dulu apakah nama/harga sudah terisi otomatis
            if (txtHarga.getText().isEmpty() || txtQty.getText().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Pilih barang dengan benar dan pastikan Qty terisi!");
                return; // Hentikan eksekusi kode di sini
            }
            
            int harga = Integer.parseInt(txtHarga.getText());
            int qty = Integer.parseInt(txtQty.getText());
            int subtotal = harga * qty;

            // Masukkan data ke tabel keranjang
            modelKeranjang.addRow(new Object[]{
                txtKode.getText(),
                txtNama.getText(),
                harga,
                qty,
                subtotal
            });

            // Panggil si kalkulator
            hitungTotal(); 

            // Bersihkan form inputan agar siap diketik barang selanjutnya
            txtKode.setText("");
            txtNama.setText("");
            txtHarga.setText("");
            txtQty.setText("");
            txtKode.requestFocus(); 
            
        } catch (Exception e) {
         // Menampilkan pesan error asli dari Java agar kita tahu masalah sebenarnya
         javax.swing.JOptionPane.showMessageDialog(this, "Error aslinya: " + e);
     }
    }//GEN-LAST:event_btnTambahActionPerformed

